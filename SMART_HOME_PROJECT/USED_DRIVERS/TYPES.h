#ifndef _COM_TYPES_H_
    #define _COM_TYPES_H_

    // for boolean types
    #define TRUE    1U
    #define False   0U

    // for unsigned types
    typedef unsigned char       u8;
    typedef unsigned short      u16;
    typedef unsigned long       u32;
    typedef unsigned long long  u64;

    // for signed types
    typedef char                s8;
    typedef short               s16;
    typedef long                s32;
    typedef long long           s64;

    // for boolean types

    typedef unsigned char       BOOL;

#endif